#   Faça um Programa que peça o raio de um círculo, calcule e mostre sua área.
raio_circulo = float(input('Informe a área do circulo a ser calculada: '))
area_circulo = 3.14 * (raio_circulo * raio_circulo)
print(f'O circulo com o valor de {raio_circulo} tem sua área com um valor de {area_circulo}!')

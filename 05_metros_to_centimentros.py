#   Faça um Programa que converta metros para centímetros.
print('<<<<<<< Bem vindo ao PyConversor >>>>>>>')
um_metro = 100
metros_informados = float(input('Informe o valor de metros que deseja converter para centímetros: '))
metros_para_centimetro = metros_informados * um_metro
print(f'Em {metros_informados} metros temos um total de {metros_para_centimetro} centímetros!')
